
public class Student extends Person{

   String program;
   int yearLevel;
   
    // PROGRAM
    void setProgram(String p){
    	this.program = p;
    	}
    	
    String getProgram(){
    	return program;
    	}	
    
    //YEAR LEVEL
    void setYearLevel(int y){
    	this.yearLevel = y;
    	}
    	
    int getYearLevel(){
    	return yearLevel;
    	}	
    
    
}